export const generateDraftOrder = (): DraftPick[] => {
  const picks: DraftPick[] = [];
  const teams = Object.keys(TEAM_NAMES);
  
  for (let round = 1; round <= 16; round++) {
    const roundTeams = round % 2 === 1 ? teams : [...teams].reverse();
    
    roundTeams.forEach((team, index) => {
      const pick = round % 2 === 1 ? index + 1 : teams.length - index;
      picks.push({
        round,
        pick: (round - 1) * teams.length + (round % 2 === 1 ? pick : teams.length - pick + 1),
        player: null,
        team
      });
    });
  }
  
  return picks;
};